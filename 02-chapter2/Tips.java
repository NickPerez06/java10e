import java.util.Scanner;

public class Tips {
	public static void main(String [] args) {
		
		//Declaring variables and Scanner object
		double gratuity, subtotal, total;
		Scanner info = new Scanner(System.in);

		//asking user for input reading it in
		System.out.print("Enter a subtotal and a gratuity rate: ");
		subtotal = info.nextDouble();
		gratuity = info.nextDouble();

		//Performing calculations and displaying result
		gratuity = gratuity/10;
		total = subtotal + gratuity;
		
		System.out.println("The gratuity is $" + gratuity + " and total is $" + total);
	}
}
